export function findBestAgentForInput(userInput: string): Agent {
  const lower = userInput.toLowerCase();
  const agents = getAllAgents();

  const scored = agents.map(agent => ({
    agent,
    score: agent.keywords.reduce(
      (acc, kw) => acc + (lower.includes(kw.toLowerCase()) ? 1 : 0),
      0
    ),
  }));

  scored.sort((a, b) => b.score - a.score);

  // à¸–à¹‰à¸² top score > 0 à¹ƒà¸Šà¹‰ agent à¸™à¸±à¹‰à¸™ à¹„à¸¡à¹ˆà¸‡à¸±à¹‰à¸™ fallback à¹„à¸› advisor
  if (scored[0].score > 0) return scored[0].agent;
  return agents.find(a => a.id === 'advisor')!;
}

